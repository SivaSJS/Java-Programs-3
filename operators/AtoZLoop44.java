package com.operators;

public class AtoZLoop44 {
    public static void main(String[] args) {

       //     Capital Letters

        System.out.print("Capital Characters:  ");
        for(char capCharacter = 'A'; capCharacter <='Z'; capCharacter++){
            System.out.print( capCharacter  + " ");
        }

        //        Small Letters

        System.out.print("\nSmall Characters: ");
        for(char smallCharacter = 'a'; smallCharacter <='z'; smallCharacter++){
            System.out.print ( smallCharacter  + " ");
        }
    }
}
